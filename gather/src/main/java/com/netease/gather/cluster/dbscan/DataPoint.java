package com.netease.gather.cluster.dbscan;

import java.util.List;

/**
 * Author: ykxu
 * Date: 13-4-24 下午5:30
 */
public class DataPoint {
    private String dataPointName; //数据点名称
    private List dimensioin; //数据点向量
    private boolean isKey; //是否聚点

    public DataPoint(List dimensioin,String dataPointName,boolean isKey){
        this.dataPointName=dataPointName;
        this.dimensioin=dimensioin;
        this.isKey=isKey;
    }

    public String getDataPointName() {
        return dataPointName;
    }

    public void setDataPointName(String dataPointName) {
        this.dataPointName = dataPointName;
    }

    public List getDimensioin() {
        return dimensioin;
    }

    public void setDimensioin(List dimensioin) {
        this.dimensioin = dimensioin;
    }

    public boolean isKey() {
        return isKey;
    }

    public void setKey(boolean key) {
        isKey = key;
    }
}