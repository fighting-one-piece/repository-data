package org.project.modules.association.apriori.data;

public class Instance {

	private String[] values = null;
	
	public Instance() {
	}
	
	public Instance(String[] values) {
		this.values = values;
	}

	public String[] getValues() {
		return values;
	}

	public void setValues(String[] values) {
		this.values = values;
	}
	
	
}
